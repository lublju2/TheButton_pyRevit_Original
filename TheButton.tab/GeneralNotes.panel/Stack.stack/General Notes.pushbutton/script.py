clr.AddReference('Microsoft.Office.Interop.Excel')
import Microsoft.Office.Interop.Excel as Excel

def read_excel_cell(path, sheet_name, addr):
    """Read a single cell from Excel, then close the app."""
    log(u"ğŸ“‚  Opening Excel workbook: {0}".format(path))
    app = Excel.ApplicationClass()
    app.Visible = False
    app.DisplayAlerts = False
    try:
        wb = app.Workbooks.Open(path)
        ws = wb.Sheets.Item[sheet_name]
        val = ws.Range[addr].Value2
        log(u"âœ…  Read {0}!{1}: {2}".format(sheet_name, addr, val))
    except Exception as ex:
        log(u"âŒ  Excel read error: {0}".format(ex))
        val = None
    finally:
        if 'wb' in locals():
            wb.Close(False)
        app.Quit()
    return val

